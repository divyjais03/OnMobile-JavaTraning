package DAY2;

public class if_else {

	public static void main(String[] args) {
		int number=10;
		
		if(number>0)
		{
			System.out.println("The number is positive");
		}
		else
		{
			System.out.println("The number is Negative");
		}
	System.out.println("Statement Outside If Else block...");
	}

}
